#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	if (n == -2147483648 || n == 2147483647)
	{
		if (n < 0)
			ft_putstr_fd("-2147483648", fd);
		else
			ft_putstr_fd("2147483647", fd);
		return ;
	}
	if (n < 0)
	{
		ft_putchar_fd('-', fd);
		n = -n;
	}
	if (n > 9)
		ft_putnbr_fd(n / 10, fd);
	ft_putchar_fd(n % 10 + '0', fd);
}
